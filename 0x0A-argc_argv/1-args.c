#include <stdio.h>

/**
  * main - print argc
  * @argc: argument count
  * @argv: arguments
  * Return: 0 for success
  */

int main(int argc, char __attribute__ ((unused)) *argv[])
{
	/* printing the number of arguments passed to main */
	/* which basically means print argc */
	printf("%d\n", argc);

	return (0);
}
